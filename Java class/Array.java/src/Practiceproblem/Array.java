package Pacticeprolem;

public class Array {
    public static void main(String[] args) {

        int [] arr = new int [] {5, 5, 8, 5, 7}; // array intialized
        System.out.println("Elements of given array: ");

        for (int i = 0; i < arr.length; i++) {//Loop through the array by incrementing value of i
            System.out.print(arr[i] + " ");
        }
    }

}

